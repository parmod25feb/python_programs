a,b = 7,8

# Sum with lambda function
sum = lambda a,b : a+b
s=sum(a,b)
print(f"Sum of {a,b} is : {s}")

# Multiplication with lambda function
mul = lambda a,b : a*b
m=mul(a,b)
print(f"MUltiplication for {a,b} is: {m}")